			i = -1,
			j = 1989,
			n = countries.length;

		//associate country names with globe
		countries = countries.filter(function(d) {
			return names.some(function(n) {
				if (d.id == n.id) return d.name = n.name;
			});
		}).sort(function(a, b) {
			return a.name.localeCompare(b.name);
		});
		
		//look for name differences between GTD and globe
		function findCountries(name) {
			for (var i = 0; i < countries.length; i++) {
				if (name == countries[i].name) return i;
				else if (name in nameDifferencesGlobe) {
					if (nameDifferencesGlobe[name][0] == countries[i].name) return i;
				}
			}
			return -1;
		}
		
