		function cumulate(country, year) {
			var cumCountry = 0;
			var cumTotal = 0;
			for (var i = 1990; i <= year; i++) {
				cumCountry += countryAttackTotals[country][i.toString()];
				cumTotal += yearlyTotals[i.toString()];
			}
			return {cc: cumCountry, ct: cumTotal};
		}
		
